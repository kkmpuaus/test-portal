CREATE TABLE `portal_login` (
`user_id` nvarchar(20) NOT NULL,
`display_name` nvarchar(30) NOT NULL,
`password` varchar(100) NOT NULL,
`sub_id` nvarchar(20) NOT NULL,
`user_role` varchar(20) NOT NULL,
`create_date` TIMESTAMP default CURRENT_TIMESTAMP,
`email` nvarchar(100) default NULL,
`pwd_reset` int(1) NOT NULL,
`pwd_modified_date` TIMESTAMP default NULL,
`login_date` TIMESTAMP default NULL,
`login_ip` varchar(20) default NULL,
`last_login_date` TIMESTAMP default NULL,
`last_login_ip` varchar(20) default NULL,
`last_failed_login_date` TIMESTAMP default NULL,
`retry` int(1) NOT NULL,
`locked` int(1) NOT NULL,
`locked_date` TIMESTAMP default NULL,
`status` char(1) NOT NULL,
PRIMARY KEY (`user_id`)
) ENGINE=InnoDB  DEFAULT CHARSET=utf8;
